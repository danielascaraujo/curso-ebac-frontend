// Criando a Função de Multiplicação
function multiplicar(numero1: number, numero2: number): number {
    return numero1 * numero2;
}

// Utilizando a Função
let resultadoMultiplicacao = multiplicar(25, 5);
console.log(resultadoMultiplicacao);

// Criando a Função de Saudação
function saudacao(nome: string): string {
    return "Olá, " + nome;
}

// Utilizando a Função
let mensagemDeSaudacao = saudacao("Marília");
console.log(mensagemDeSaudacao);